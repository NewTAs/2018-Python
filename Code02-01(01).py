## 변수 선언 부분 ##
a = 100    #a라는 변수에 100 대입
b = 50    #b라는 변수에 50 대입

## 메인 코드 부분 ##
result = a + b    #result라는 함수에 a+b를 더한 값을 대입
print(a, "+", b, "=", result)    #a, b, result 출력
result = a - b    #result라는 함수에 a-b를 더한 값을 대입
print(a, "-", b, "=", result)    #a, b, result 출력
result = a * b    #result라는 함수에 a*b를 더한 값을 대입
print(a, "*", b, "=", result)    #a, b, result 출력
result = a / b    #result라는 함수에 a/b를 더한 값을 대입
print(a, "/", b, "=", result)    #a, b, result 출력
