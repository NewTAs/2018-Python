import turtle    #turtle 함수를 불러온다.

##메인 함수 부분##
turtle.shape("turtle")    #turtle 모양 - turtle

turtle.forward(200)    #turtle 앞으로 200
turtle.right(90)    #turtle 오른쪽으로 90도 회전
turtle.forward(200)    #turtle 앞으로 200
turtle.right(90)    #turtle 오른쪽으로 90도 회전
turtle.forward(200)    #turtle 앞으로 200
turtle.right(90)    #turtle 오른쪽으로 90도 회전
turtle.forward(200)    #turtle 앞으로 200

turtle.done()    #turtle 프로그램을 종료한다.
