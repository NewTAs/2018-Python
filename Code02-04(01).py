## 변수 선언 부분 ##
a = int(input("첫 번째 정수를 입력해주세요. :"))    #첫 번째 정수를 입력받는다.
b = int(input("두 번째 정수를 입력해주세요. :"))    #두 번째 정수를 입력받는다.

## 메인 코드 부분 ##
result = a + b    #result라는 함수에 a+b를 더한 값을 대입
print(a, "+", b, "=", result)    #a, b, result 출력
result = a - b    #result라는 함수에 a-b를 더한 값을 대입
print(a, "-", b, "=", result)    #a, b, result 출력
result = a * b    #result라는 함수에 a*b를 더한 값을 대입
print(a, "*", b, "=", result)    #a, b, result 출력
result = a / b    #result라는 함수에 a/b를 더한 값을 대입
print(a, "/", b, "=", result)    #a, b, result 출력
